using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp1
{
    public class Sac
    {
        private List<Materiel> stuff;

        public List<Materiel> Stuff
        {
            get => stuff;
            set => stuff = value;
        }

        public void seDecrire()
        {
            for (int i = 0; i < stuff.Count; i++)
            {
                stuff[i].SeDecrire();
            }
        }
    }
}
